function parsePositiveInt(
  raw: string | null,
  fallback: number,
  maxValue: number,
): number {
  const n = Number(raw ?? fallback);
  if (!Number.isFinite(n) || n <= 0) return fallback;
  return Math.min(Math.floor(n), maxValue);
}

function toNumberSafe(value: unknown): number | null {
  const n = Number(value);
  return Number.isFinite(n) ? n : null;
}

function parseMinutes(value: unknown): number | null {
  if (typeof value === "number" && Number.isFinite(value)) return value;
  if (typeof value !== "string") return null;
  const raw = value.trim();
  if (!raw) return null;
  if (raw.includes(":")) {
    const [m, s] = raw.split(":");
    const mm = Number(m);
    const ss = Number(s);
    if (Number.isFinite(mm) && Number.isFinite(ss)) return mm + ss / 60;
  }
  const n = Number(raw);
  return Number.isFinite(n) ? n : null;
}

function toText(value: unknown): string | null {
  if (typeof value === "string") return value;
  if (typeof value === "number") return String(value);
  return null;
}

function buildPlayedRowsForGame(
  game: InternalGame,
  stats: UpstreamPlayerStat[],
  seasonForLogs: string,
): PlayedLogRow[] {
  const gameId = Number(game?.id ?? NaN);
  if (!Number.isFinite(gameId) || gameId <= 0) return [];

  const gameDate =
    typeof game?.date === "string" && Number.isFinite(Date.parse(game.date))
      ? new Date(game.date).toISOString()
      : null;
  const homeId = toNumberSafe(game?.teams?.home?.id);
  const awayId = toNumberSafe(game?.teams?.away?.id);
  const homeName = toText(game?.teams?.home?.name);
  const awayName = toText(game?.teams?.away?.name);
  const homeCode = toText(game?.teams?.home?.code);
  const awayCode = toText(game?.teams?.away?.code);
  const homeScore = toNumberSafe((game as any)?.scores?.home?.total);
  const awayScore = toNumberSafe((game as any)?.scores?.away?.total);
  const score =
    homeScore !== null && awayScore !== null ? `${homeScore}-${awayScore}` : null;
  const leagueId = toNumberSafe((game as any)?.league?.id);
  const statusShort = toText((game as any)?.status?.short);

  const rows: PlayedLogRow[] = [];
  for (const stat of stats) {
    const playerId = toNumberSafe(stat?.player?.id);
    if (!playerId || playerId <= 0) continue;
    const minutes = parseMinutes(stat?.min);
    const hasAction =
      (minutes !== null && minutes > 0) ||
      [stat?.points, stat?.assists, stat?.totReb, stat?.fga, stat?.fta, stat?.tpa]
        .map((v) => toNumberSafe(v))
        .some((v) => v !== null && v > 0);
    if (!hasAction) continue;

    const teamId = toNumberSafe(stat?.team?.id);
    const teamName = toText(stat?.team?.name);
    const teamCode = toText(stat?.team?.code);
    let homeAway: "home" | "away" | "unknown" = "unknown";
    if (teamId !== null && homeId !== null && teamId === homeId) homeAway = "home";
    if (teamId !== null && awayId !== null && teamId === awayId) homeAway = "away";

    const opponentTeamId =
      homeAway === "home" ? awayId : homeAway === "away" ? homeId : null;
    const opponentTeamName =
      homeAway === "home" ? awayName : homeAway === "away" ? homeName : null;
    const opponentTeamCode =
      homeAway === "home" ? awayCode : homeAway === "away" ? homeCode : null;

    let result: "W" | "L" | "NA" = "NA";
    if (
      homeAway !== "unknown" &&
      homeScore !== null &&
      awayScore !== null &&
      homeScore !== awayScore
    ) {
      const won =
        (homeAway === "home" && homeScore > awayScore) ||
        (homeAway === "away" && awayScore > homeScore);
      result = won ? "W" : "L";
    }

    rows.push({
      player_id: playerId,
      season: seasonForLogs,
      game_id: gameId,
      date: gameDate,
      team_id: teamId,
      team_code: teamCode,
      team_name: teamName,
      opponent_team_id: opponentTeamId,
      opponent_team_code: opponentTeamCode,
      opponent_team_name: opponentTeamName,
      home_away: homeAway,
      result,
      score,
      score_home: homeScore,
      score_away: awayScore,
      points: toNumberSafe(stat?.points),
      rebounds: toNumberSafe(stat?.totReb),
      assists: toNumberSafe(stat?.assists),
      minutes,
      field_goals_made: toNumberSafe(stat?.fgm),
      field_goals_attempted: toNumberSafe(stat?.fga),
      field_goal_pct: toNumberSafe(stat?.fgp),
      three_points_made: toNumberSafe(stat?.tpm),
      three_points_attempted: toNumberSafe(stat?.tpa),
      three_point_pct: toNumberSafe(stat?.tpp),
      free_throws_made: toNumberSafe(stat?.ftm),
      free_throws_attempted: toNumberSafe(stat?.fta),
      free_throw_pct: toNumberSafe(stat?.ftp),
      is_preseason: false,
      league_id: leagueId,
      status_short: statusShort,
    });
  }

  return rows;
}

