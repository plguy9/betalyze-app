create table if not exists nba_picks_results_daily (
  id bigint generated by default as identity primary key,
  date text not null,
  grade text not null,
  hits integer not null default 0,
  total integer not null default 0,
  hit_rate integer not null default 0,
  roi integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (date, grade)
);

create index if not exists nba_picks_results_daily_date_idx
  on nba_picks_results_daily (date desc);
