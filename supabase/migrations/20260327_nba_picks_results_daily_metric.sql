create table if not exists nba_picks_results_daily_metric (
  id bigint generated by default as identity primary key,
  date text not null,
  metric text not null,
  hits integer not null default 0,
  total integer not null default 0,
  hit_rate integer not null default 0,
  games integer not null default 0,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (date, metric)
);

create index if not exists nba_picks_results_daily_metric_date_idx
  on nba_picks_results_daily_metric (date desc);
