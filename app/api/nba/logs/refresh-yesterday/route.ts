function torontoYmd(date = new Date()): string {
  return new Intl.DateTimeFormat("en-CA", {
    timeZone: DEFAULT_TIMEZONE,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(date);
}

function addDaysYmd(ymd: string, days: number): string {
  const ts = Date.parse(`${ymd}T12:00:00Z`);
  if (!Number.isFinite(ts)) return ymd;
  const next = new Date(ts + days * 24 * 60 * 60 * 1000);
  const y = next.getUTCFullYear();
  const m = String(next.getUTCMonth() + 1).padStart(2, "0");
  const d = String(next.getUTCDate()).padStart(2, "0");
  return `${y}-${m}-${d}`;
}

function torontoYmdFromIso(raw: string | null): string | null {
  if (!raw) return null;
  const ts = Date.parse(raw);
  if (!Number.isFinite(ts)) return null;
  return torontoYmd(new Date(ts));
}

async function refreshPlayerSummary(
  origin: string,
  season: string,
  playerId: number,
  options?: { expectedDate?: string | null; strictDate?: boolean },
): Promise<RefreshResult> {
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), DEFAULT_TIMEOUT_MS);
  try {
    const url = new URL(`/api/nba/players/${playerId}/summary`, origin);
    url.searchParams.set("season", season);
    url.searchParams.set("refresh", "1");
    const res = await fetch(url.toString(), {
      cache: "no-store",
      signal: controller.signal,
    });
    if (!res.ok) return { playerId, ok: false, status: res.status };
    const payload = (await res.json().catch(() => null)) as
      | { summary?: { games?: unknown[] } }
      | null;
    const games = Array.isArray(payload?.summary?.games) ? payload.summary!.games! : [];
    const maxDate =
      games.length > 0
        ? games
            .map((g) =>
              typeof (g as { date?: unknown })?.date === "string"
                ? ((g as { date: string }).date as string)
                : null,
            )
            .find((d) => d && Number.isFinite(Date.parse(d))) ?? null
        : null;
    const maxDateToronto = torontoYmdFromIso(maxDate);
    if (options?.strictDate && options.expectedDate && maxDateToronto !== options.expectedDate) {
      return {
        playerId,
        ok: false,
        games: games.length,
        maxDate,
        maxDateToronto,
        error: "stale_after_refresh",
      };
    }
    return { playerId, ok: true, games: games.length, maxDate, maxDateToronto };
  } catch (err) {
    const abort =
      err instanceof DOMException
        ? err.name === "AbortError"
        : String(err).toLowerCase().includes("abort");
    return {
      playerId,
      ok: false,
      error: abort ? "timeout" : err instanceof Error ? err.message : String(err),
    };
  } finally {
    clearTimeout(timeout);
  }
}

async function syncPlayerTeamsFromLatestLogs(
  seasons: string[],
  playerIds: number[],
): Promise<{ updated: number }> {
  if (!playerIds.length || !seasons.length) return { updated: 0 };

  const rows = await prisma.$queryRaw<Array<{ player_id: number }>>(
    Prisma.sql`
      with latest_logs as (
        select distinct on (l.player_id)
          l.player_id,
          l.team_id,
          l.team_code,
          l.team_name
        from nba_player_game_logs l
        where l.season in (${Prisma.join(seasons.map((s) => Prisma.sql`${s}`))})
          and l.player_id in (${Prisma.join(playerIds.map((id) => Prisma.sql`${id}`))})
          and (
            l.team_id is not null
            or nullif(trim(coalesce(l.team_code, '')), '') is not null
          )
        order by l.player_id, l.date desc nulls last, l.game_id desc
      )
      update nba_players p
      set
        team_code = ll.team_code,
        team_name = coalesce(ll.team_name, p.team_name),
        updated_at = now()
      from latest_logs ll
      where p.season in (${Prisma.join(seasons.map((s) => Prisma.sql`${s}`))})
        and p.player_id = ll.player_id
        and (
          coalesce(p.team_code, '') is distinct from coalesce(ll.team_code, '')
          or coalesce(p.team_name, '') is distinct from coalesce(ll.team_name, '')
        )
      returning p.player_id
    `,
  );

  return { updated: rows.length };
}

function buildSeasonAliases(input: string): string[] {
  const raw = String(input ?? "").trim();
  if (!raw) return [];
  const normalized = normalizeNbaSeasonLabel(raw);
  const year = raw.match(/(\d{4})/)?.[1] ?? normalized.match(/(\d{4})/)?.[1] ?? null;
  const span = year ? `${year}-${Number(year) + 1}` : null;
  return Array.from(new Set([raw, normalized, year, span].filter(Boolean))) as string[];
}

export async function GET(req: NextRequest) {
  const cronSecret = process.env.CRON_SECRET;
  if (cronSecret) {
    const authHeader = req.headers.get("authorization") ?? "";
    const customHeader = req.headers.get("x-cron-secret") ?? "";
    const querySecret = req.nextUrl.searchParams.get("secret") ?? "";
    const valid =
      authHeader === `Bearer ${cronSecret}` ||
      customHeader === cronSecret ||
      querySecret === cronSecret;
    if (!valid) {
      return NextResponse.json({ error: "Unauthorized" }, { status: 401 });
    }
  }

  const season = normalizeNbaSeasonLabel(
    req.nextUrl.searchParams.get("season") ?? DEFAULT_SEASON,
  );
  const leagueParam = String(
    req.nextUrl.searchParams.get("league") ?? DEFAULT_GAMES_LEAGUE,
  )
    .trim()
    .toLowerCase();
  const gamesLeague =
    !leagueParam || leagueParam === "nba" || leagueParam === "12"
      ? "standard"
      : leagueParam;
  const seasonAliases = buildSeasonAliases(season);
  const date =
    req.nextUrl.searchParams.get("date") ?? addDaysYmd(torontoYmd(), -1);
  const concurrency = parsePositiveInt(
    req.nextUrl.searchParams.get("concurrency"),
    DEFAULT_CONCURRENCY,
    MAX_CONCURRENCY,
  );
  const maxPlayers = parsePositiveInt(
    req.nextUrl.searchParams.get("max"),
    5000,
    5000,
  );
  const targetModeRaw = String(
    req.nextUrl.searchParams.get("mode") ?? DEFAULT_TARGET_MODE,
  )
    .trim()
    .toLowerCase();
  const targetMode =
    targetModeRaw === "rotation"
      ? "rotation"
      : targetModeRaw === "played"
        ? "played"
        : "full";
  const rotationPerTeam = parsePositiveInt(
    req.nextUrl.searchParams.get("perTeam"),
    DEFAULT_ROTATION_PER_TEAM,
    MAX_ROTATION_PER_TEAM,
  );
  const rotationMinGp = parsePositiveInt(
    req.nextUrl.searchParams.get("minGp"),
    DEFAULT_ROTATION_MIN_GP,
    MAX_ROTATION_MIN_GP,
  );
  const dryRun = req.nextUrl.searchParams.get("dry") === "1";
  const includeDetails = req.nextUrl.searchParams.get("details") === "1";
  const refreshRoster = req.nextUrl.searchParams.get("refreshRoster") !== "0";
  const startedAt = Date.now();

  try {
    const gamesUrl = new URL("/api/nba/games", req.nextUrl.origin);
    gamesUrl.searchParams.set("date", date);
    gamesUrl.searchParams.set("season", season);
    gamesUrl.searchParams.set("league", gamesLeague);
    gamesUrl.searchParams.set("timezone", DEFAULT_TIMEZONE);

    const gamesRes = await fetch(gamesUrl.toString(), {
      cache: "no-store",
      signal: AbortSignal.timeout(20_000),
    });
    if (!gamesRes.ok) {
      return NextResponse.json(
        {
          ok: false,
          error: "Failed to load games for target date",
          status: gamesRes.status,
          date,
          season,
        },
        { status: 502 },
      );
    }

    const gamesPayload = (await gamesRes.json().catch(() => null)) as
      | InternalGamesPayload
      | null;
    const games = Array.isArray(gamesPayload?.response)
      ? gamesPayload!.response!
      : [];
    const finishedGames = games.filter((g) =>
      FINISHED_STATUSES.has(String(g?.status?.short ?? "").toUpperCase()),
    );
    const gamesForScope = finishedGames.length ? finishedGames : games;

    const teamIds = new Set<number>();
    const teamCodes = new Set<string>();
    for (const game of gamesForScope) {
      const homeId = Number(game?.teams?.home?.id ?? NaN);
      const awayId = Number(game?.teams?.away?.id ?? NaN);
      if (Number.isFinite(homeId) && homeId > 0) teamIds.add(homeId);
      if (Number.isFinite(awayId) && awayId > 0) teamIds.add(awayId);

      const homeCode = String(game?.teams?.home?.code ?? "")
        .trim()
        .toUpperCase();
      const awayCode = String(game?.teams?.away?.code ?? "")
        .trim()
        .toUpperCase();
      if (homeCode) teamCodes.add(homeCode);
      if (awayCode) teamCodes.add(awayCode);
    }
    const seasonYear = season.match(/(\d{4})/)?.[1] ?? season;
    const playedRowsAll: PlayedLogRow[] = [];
    const playedGameFetchFailures: Array<{ gameId: number; status?: number; error?: string }> =
      [];

    if (targetMode === "played") {
      if (!UPSTREAM_API_KEY) {
        return NextResponse.json(
          { ok: false, error: "Missing APISPORTS_KEY for played sync mode" },
          { status: 500 },
        );
      }
      for (const game of gamesForScope) {
        const gameId = Number(game?.id ?? NaN);
        if (!Number.isFinite(gameId) || gameId <= 0) continue;
        try {
          const url = new URL("/players/statistics", UPSTREAM_NBA_API_BASE);
          url.searchParams.set("game", String(gameId));
          url.searchParams.set("season", seasonYear);
          const res = await fetch(url.toString(), {
            headers: { "x-apisports-key": UPSTREAM_API_KEY },
            cache: "no-store",
            signal: AbortSignal.timeout(20_000),
          });
          if (!res.ok) {
            playedGameFetchFailures.push({ gameId, status: res.status });
            continue;
          }
          const payload = (await res.json().catch(() => null)) as
            | { response?: UpstreamPlayerStat[] }
            | null;
          const stats = Array.isArray(payload?.response) ? payload.response : [];
          playedRowsAll.push(...buildPlayedRowsForGame(game, stats, season));
        } catch (err) {
          playedGameFetchFailures.push({
            gameId,
            error: err instanceof Error ? err.message : String(err),
          });
        }
      }
    }

    const seasonSql = Prisma.join(seasonAliases.map((s) => Prisma.sql`${s}`));
    const collectTargetedPlayerIds = async (): Promise<number[]> => {
      const playerIds = new Set<number>();
      const addRows = (rows: Array<{ player_id: number }>) => {
        for (const row of rows) {
          const id = Number(row.player_id);
          if (Number.isFinite(id) && id > 0) playerIds.add(id);
        }
      };

      if (targetMode === "played") {
        for (const row of playedRowsAll) {
          if (row.player_id > 0) playerIds.add(row.player_id);
        }
      } else if (targetMode === "rotation" && teamIds.size > 0) {
        const ids = Array.from(teamIds.values());
        const rows = await prisma.$queryRaw<Array<{ player_id: number }>>`
          with base as (
            select
              l.team_id,
              l.player_id,
              l.minutes,
              row_number() over (
                partition by l.team_id, l.player_id
                order by l.date desc nulls last, l.game_id desc
              ) as rn
            from nba_player_game_logs l
            where l.season in (${seasonSql})
              and l.team_id in (${Prisma.join(ids)})
              and coalesce(l.is_preseason, false) = false
              and coalesce(l.minutes, 0) > 0
          ),
          agg as (
            select
              team_id,
              player_id,
              avg(minutes) filter (where rn <= 10) as avg_min_l10,
              count(*) filter (where rn <= 10) as gp_l10
            from base
            group by team_id, player_id
          ),
          ranked as (
            select
              team_id,
              player_id,
              row_number() over (
                partition by team_id
                order by coalesce(avg_min_l10, 0) desc, gp_l10 desc, player_id asc
              ) as rnk
            from agg
            where gp_l10 >= ${rotationMinGp}
          )
          select distinct player_id
          from ranked
          where rnk <= ${rotationPerTeam}
        `;
        addRows(rows);
      } else if (teamIds.size > 0) {
        const ids = Array.from(teamIds.values());
        const rows = await prisma.$queryRaw<Array<{ player_id: number }>>`
          select distinct player_id
          from nba_players
          where season in (${seasonSql})
            and team_id in (${Prisma.join(ids)})
        `;
        addRows(rows);
        if (teamCodes.size > 0) {
          const codes = Array.from(teamCodes.values());
          const codeRows = await prisma.$queryRaw<Array<{ player_id: number }>>`
            select distinct player_id
            from nba_players
            where season in (${seasonSql})
              and upper(coalesce(team_code, '')) in (${Prisma.join(codes.map((c) => Prisma.sql`${c}`))})
          `;
          addRows(codeRows);
        }
      }

      if (targetMode !== "played") {
        // Fallback safety for non-played modes: include players already logged on target date.
        const logRows = await prisma.$queryRaw<Array<{ player_id: number }>>`
          select distinct player_id
          from nba_player_game_logs
          where season in (${Prisma.join(seasonAliases.map((s) => Prisma.sql`${s}`))})
            and (date at time zone ${DEFAULT_TIMEZONE})::date = ${date}::date
        `;
        addRows(logRows);
      }

      return Array.from(playerIds.values()).slice(0, maxPlayers);
    };

    const allPlayerIds = await collectTargetedPlayerIds();
    if (dryRun) {
      return NextResponse.json({
        ok: true,
        mode: "dry-run",
        season,
        date,
        gamesFound: games.length,
        finishedGames: finishedGames.length,
        teamsFound: teamIds.size,
        playersTargeted: allPlayerIds.length,
        targetMode,
        rotationPerTeam,
        rotationMinGp,
        refreshRoster,
        durationMs: Date.now() - startedAt,
      });
    }

    let rosterRefreshResult: RosterRefreshResult = {
      attempted: false,
      ok: true,
      status: null,
      count: null,
      dbUpserted: null,
      error: null,
    };

    if (refreshRoster) {
      rosterRefreshResult.attempted = true;
      try {
        const syncUrl = new URL("/api/nba/sync-players-v2", req.nextUrl.origin);
        const syncRes = await fetch(syncUrl.toString(), {
          cache: "no-store",
          signal: AbortSignal.timeout(120_000),
        });
        const syncJson = (await syncRes.json().catch(() => null)) as
          | { ok?: boolean; count?: number; dbUpserted?: number; error?: string }
          | null;
        rosterRefreshResult = {
          attempted: true,
          ok: Boolean(syncRes.ok && syncJson?.ok),
          status: syncRes.status,
          count:
            typeof syncJson?.count === "number" && Number.isFinite(syncJson.count)
              ? syncJson.count
              : null,
          dbUpserted:
            typeof syncJson?.dbUpserted === "number" && Number.isFinite(syncJson.dbUpserted)
              ? syncJson.dbUpserted
              : null,
          error:
            syncRes.ok && syncJson?.ok
              ? null
              : (syncJson?.error ?? `sync-players-v2 failed (${syncRes.status})`),
        };
      } catch (err) {
        rosterRefreshResult = {
          attempted: true,
          ok: false,
          status: null,
          count: null,
          dbUpserted: null,
          error: err instanceof Error ? err.message : String(err),
        };
      }
    }
    const targetPlayerIds =
      refreshRoster && rosterRefreshResult.ok
        ? await collectTargetedPlayerIds()
        : allPlayerIds;

    let processed = 0;
    let success = 0;
    let failed = 0;
    const failedDetails: RefreshResult[] = [];
    const successPlayerIds: number[] = [];
    if (targetMode === "played") {
      const targetSet = new Set(targetPlayerIds);
      const dedupedRowMap = new Map<string, PlayedLogRow>();
      for (const row of playedRowsAll) {
        if (!targetSet.has(row.player_id)) continue;
        dedupedRowMap.set(`${row.player_id}:${row.game_id}`, row);
      }
      const rowsToUpsert = Array.from(dedupedRowMap.values());
      await upsertPlayedRows(rowsToUpsert);

      processed = targetPlayerIds.length;
      const successSet = new Set(rowsToUpsert.map((row) => row.player_id));
      success = successSet.size;
      failed = Math.max(0, processed - success);
      successPlayerIds.push(...Array.from(successSet.values()));

      if (includeDetails && failed > 0) {
        for (const playerId of targetPlayerIds) {
          if (successSet.has(playerId)) continue;
          if (failedDetails.length >= 250) break;
          failedDetails.push({
            playerId,
            ok: false,
            error: "missing_played_rows_for_player",
          });
        }
      }
    } else {
      for (let i = 0; i < targetPlayerIds.length; i += concurrency) {
        const slice = targetPlayerIds.slice(i, i + concurrency);
        const results = await Promise.all(
          slice.map((playerId) =>
            refreshPlayerSummary(req.nextUrl.origin, season, playerId, {
              strictDate: false,
              expectedDate: null,
            }),
          ),
        );
        processed += results.length;
        for (const result of results) {
          if (result.ok) {
            success += 1;
            successPlayerIds.push(result.playerId);
          } else {
            failed += 1;
            if (failedDetails.length < 250) failedDetails.push(result);
          }
        }
      }
    }

    const rosterSync = await syncPlayerTeamsFromLatestLogs(seasonAliases, successPlayerIds);

    const coverageRows = await prisma.$queryRaw<
      Array<{ players: number; max_date: string | null }>
    >`
      select
        count(distinct player_id)::int as players,
        max(date)::text as max_date
      from nba_player_game_logs
      where season in (${Prisma.join(seasonAliases.map((s) => Prisma.sql`${s}`))})
    `;

    return NextResponse.json({
      ok: true,
      mode: "refresh-yesterday",
      season,
      date,
      gamesFound: games.length,
      finishedGames: finishedGames.length,
      teamsFound: teamIds.size,
      playersTargeted: targetPlayerIds.length,
      targetMode,
      rotationPerTeam,
      rotationMinGp,
      processed,
      success,
      failed,
      rosterTeamsUpdated: rosterSync.updated,
      rosterRefresh: rosterRefreshResult,
      logsPlayersCoverage: Number(coverageRows[0]?.players ?? 0),
      logsMaxDate: coverageRows[0]?.max_date ?? null,
      playedGamesFetched:
        targetMode === "played"
          ? Math.max(
              0,
              gamesForScope.length - playedGameFetchFailures.length,
            )
          : undefined,
      playedGamesFailed:
        targetMode === "played" ? playedGameFetchFailures.length : undefined,
      durationMs: Date.now() - startedAt,
      details: includeDetails ? failedDetails : undefined,
    });
  } catch (err) {
    return NextResponse.json(
      {
        ok: false,
        error: err instanceof Error ? err.message : String(err),
        season,
        date,
      },
      { status: 500 },
    );
  }
}
